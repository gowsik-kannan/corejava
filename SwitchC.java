
public class SwitchC {
	public static void main(String s[]) {
		int age=18;
		switch(age) {
		case 10:
			System.out.println("child");
			break;
		case 18:
			System.out.println("Adult");
			break;
		case 70:
			System.out.println("old");
			break;
		 default:
			 System.out.println("default");
	break;
		}
	}
}
